-- Owner kendi firması için davet oluşturabilir
DROP POLICY IF EXISTS "firma_davetleri_owner_insert" ON public.firma_davetleri;
CREATE POLICY "firma_davetleri_owner_insert"
  ON public.firma_davetleri FOR INSERT
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.kurumsal_firma_yoneticileri
      WHERE user_id = auth.uid()
        AND firma_id = firma_davetleri.firma_id
        AND role = 'owner'
    )
  );

-- Owner kendi firmasının davetlerini görebilir ve güncelleyebilir (iptal)
DROP POLICY IF EXISTS "firma_davetleri_owner_select" ON public.firma_davetleri;
CREATE POLICY "firma_davetleri_owner_select"
  ON public.firma_davetleri FOR SELECT
  USING (
    EXISTS (
      SELECT 1 FROM public.kurumsal_firma_yoneticileri
      WHERE user_id = auth.uid()
        AND firma_id = firma_davetleri.firma_id
        AND role = 'owner'
    )
    OR invited_user_id = auth.uid()
  );

DROP POLICY IF EXISTS "firma_davetleri_owner_update" ON public.firma_davetleri;
CREATE POLICY "firma_davetleri_owner_update"
  ON public.firma_davetleri FOR UPDATE
  USING (
    -- Owner iptal edebilir
    EXISTS (
      SELECT 1 FROM public.kurumsal_firma_yoneticileri
      WHERE user_id = auth.uid()
        AND firma_id = firma_davetleri.firma_id
        AND role = 'owner'
    )
    -- Davet edilen kabul/ret edebilir
    OR invited_user_id = auth.uid()
  );

