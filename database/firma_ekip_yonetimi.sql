-- Kullanıcının firmadaki rolünü döndürür (null = üye değil)
CREATE OR REPLACE FUNCTION public.get_user_firma_role(p_firma_id text)
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT role
  FROM public.kurumsal_firma_yoneticileri
  WHERE user_id = auth.uid()
    AND firma_id = p_firma_id
  LIMIT 1;
$$;

-- Kullanıcı bu firmada en az belirtilen rolde mi?
-- owner > admin > member hiyerarşisi
CREATE OR REPLACE FUNCTION public.user_has_firma_role(p_firma_id text, p_min_role text)
RETURNS boolean
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.kurumsal_firma_yoneticileri
    WHERE user_id = auth.uid()
      AND firma_id = p_firma_id
      AND CASE p_min_role
            WHEN 'member' THEN role IN ('owner', 'admin', 'member')
            WHEN 'admin'  THEN role IN ('owner', 'admin')
            WHEN 'owner'  THEN role = 'owner'
            ELSE false
          END
  );
$$;

-- Davet kabul edildiğinde kurumsal_firma_yoneticileri'ne ekle (SECURITY DEFINER)
CREATE OR REPLACE FUNCTION public.accept_firma_daveti(p_davet_id bigint)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_davet record;
  v_existing_firma text;
BEGIN
  -- Daveti bul ve kilitle
  SELECT * INTO v_davet
  FROM public.firma_davetleri
  WHERE id = p_davet_id
    AND invited_user_id = auth.uid()
    AND status = 'pending'
    AND expires_at > now()
  FOR UPDATE;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('error', 'Davet bulunamadı veya süresi dolmuş.');
  END IF;

  -- Kullanıcı zaten başka bir firmada mı?
  SELECT firma_id INTO v_existing_firma
  FROM public.kurumsal_firma_yoneticileri
  WHERE user_id = auth.uid()
  LIMIT 1;

  IF v_existing_firma IS NOT NULL THEN
    RETURN jsonb_build_object('error', 'Zaten bir firmaya bağlısınız.');
  END IF;

  -- Firmaya ekle
  INSERT INTO public.kurumsal_firma_yoneticileri (user_id, firma_id, role, title, page_permissions)
  VALUES (auth.uid(), v_davet.firma_id, v_davet.role, v_davet.title,
    COALESCE(v_davet.page_permissions, '{"firma_paneli": false, "teklif_yonetimi": true, "ihale_yonetimi": true, "ekip_yonetimi": false}'::jsonb));

  -- Daveti kabul edildi olarak işaretle
  UPDATE public.firma_davetleri
  SET status = 'accepted', updated_at = now()
  WHERE id = p_davet_id;

  RETURN jsonb_build_object('success', true, 'firma_id', v_davet.firma_id, 'role', v_davet.role);
END;
$$;

