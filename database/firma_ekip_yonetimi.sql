-- ═══════════════════════════════════════════════════════
-- 2) firma_davetleri TABLOSU
-- ═══════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.firma_davetleri (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  firma_id      text NOT NULL,
  invited_email text NOT NULL,
  invited_user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE,
  invited_by    uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  role          text NOT NULL DEFAULT 'member'
                  CHECK (role IN ('admin', 'member')),
  title         text DEFAULT NULL,
  status        text NOT NULL DEFAULT 'pending'
                  CHECK (status IN ('pending', 'accepted', 'rejected', 'cancelled')),
  created_at    timestamptz NOT NULL DEFAULT now(),
  updated_at    timestamptz NOT NULL DEFAULT now(),
  expires_at    timestamptz NOT NULL DEFAULT (now() + interval '7 days')
);

-- Aynı firmaya aynı e-posta için sadece 1 bekleyen davet
CREATE UNIQUE INDEX IF NOT EXISTS idx_firma_davetleri_pending_unique
  ON public.firma_davetleri (firma_id, lower(invited_email))
  WHERE status = 'pending';

CREATE INDEX IF NOT EXISTS idx_firma_davetleri_firma_id
  ON public.firma_davetleri (firma_id);
CREATE INDEX IF NOT EXISTS idx_firma_davetleri_invited_user
  ON public.firma_davetleri (invited_user_id);
CREATE INDEX IF NOT EXISTS idx_firma_davetleri_invited_email
  ON public.firma_davetleri (lower(invited_email));

DROP TRIGGER IF EXISTS set_firma_davetleri_updated_at ON public.firma_davetleri;
CREATE TRIGGER set_firma_davetleri_updated_at
  BEFORE UPDATE ON public.firma_davetleri
  FOR EACH ROW
  EXECUTE FUNCTION public.set_current_timestamp_updated_at();

ALTER TABLE public.firma_davetleri ENABLE ROW LEVEL SECURITY;

-- ═══════════════════════════════════════════════════════
-- 3) firma_davetleri RLS POLİCY'LERİ
