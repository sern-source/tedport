-- Aynı firmadaki herkes birbirini görebilsin (ekip sayfası için)
-- NOT: Recursive olmaması için get_my_firma_id() SECURITY DEFINER fonksiyonu kullanılır
CREATE OR REPLACE FUNCTION public.get_my_firma_id()
RETURNS text
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT firma_id
  FROM public.kurumsal_firma_yoneticileri
  WHERE user_id = auth.uid()
  LIMIT 1;
$$;

DROP POLICY IF EXISTS "firma_yoneticileri_ekip_select" ON public.kurumsal_firma_yoneticileri;
CREATE POLICY "firma_yoneticileri_ekip_select"
  ON public.kurumsal_firma_yoneticileri FOR SELECT
  USING (
    auth.uid() = user_id
    OR firma_id = public.get_my_firma_id()
    OR public.is_current_user_admin()
  );

-- Owner yeni üye ekleyebilir (davet kabul akışında kullanılır)
DROP POLICY IF EXISTS "firma_yoneticileri_owner_insert" ON public.kurumsal_firma_yoneticileri;
CREATE POLICY "firma_yoneticileri_owner_insert"
  ON public.kurumsal_firma_yoneticileri FOR INSERT
  WITH CHECK (public.is_current_user_admin());

-- Owner üye rolünü/unvanını güncelleyebilir (kendi kaydı hariç)
DROP POLICY IF EXISTS "firma_yoneticileri_owner_update" ON public.kurumsal_firma_yoneticileri;
CREATE POLICY "firma_yoneticileri_owner_update"
  ON public.kurumsal_firma_yoneticileri FOR UPDATE
  USING (
    public.is_current_user_admin()
    -- Kullanıcı kendi unvanını güncelleyebilir
    OR auth.uid() = user_id
  );

-- Owner üyeyi silebilir (kendini silemesin)
DROP POLICY IF EXISTS "firma_yoneticileri_owner_delete" ON public.kurumsal_firma_yoneticileri;
CREATE POLICY "firma_yoneticileri_owner_delete"
  ON public.kurumsal_firma_yoneticileri FOR DELETE
  USING (
    public.is_current_user_admin()
    OR (
      auth.uid() != user_id
      AND EXISTS (
        SELECT 1 FROM public.kurumsal_firma_yoneticileri kfy2
        WHERE kfy2.user_id = auth.uid()
          AND kfy2.firma_id = kurumsal_firma_yoneticileri.firma_id
          AND kfy2.role = 'owner'
      )
    )
  );

